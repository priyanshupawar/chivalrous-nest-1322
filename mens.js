function DisplymensFunc(mensData) {
  mensData.forEach(function (ele, index) {
    let div = document.createElement("div");
    let image = document.createElement("img");
    image.setAttribute("src", ele.image);
    let heading = document.createElement("h4");
    heading.innerText = ele.productName;
    let price = document.createElement("h5");
    price.innerText = ele.productPrice;
    let btn = document.createElement("button");
    btn.innerText = "Add To Cart";
    btn.addEventListener("click", function (ele) {
      addTodata.push(ele);
      localStorage.setItem("Add-cart", JSON.stringify(addTodata));
    });

    div.append(image, heading, price, btn);
    document.querySelector("#container").append(div);
  });
}

